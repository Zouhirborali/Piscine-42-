#include <unistd.h>

void	p(char c[])
{
	int	x;

	x = 0;
	while (c[x])
	{
		write(1, &c[x], 1);
		x++;
	}
	write(1, "\n", 1);
}

int	main(int ac, char **av)
{
	int	x;

	x = ac;
	p(av[0]);
	return (0);
}
