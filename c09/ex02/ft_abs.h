#ifndef FT_ABS_H
# define FT_ABS_H
# define ABS(Value)		\
({						\
	int x;				\
	if (Value > 0)		\
	{					\
		x = Value;		\
	}					\
	else				\
	{					\
		x = Value * -1;	\
	}					\
	x;					\
})						\


#endif