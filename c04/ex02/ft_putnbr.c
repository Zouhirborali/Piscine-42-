#include <unistd.h>

void	w(char r)
{
	write(1, &r, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		w('-');
		nb *= -1;
	}
	if (nb == -2147483648)
	{
		w('2');
		ft_putnbr(147483648);
	}
	else if (nb <= 9)
	{
		w(nb + '0');
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
